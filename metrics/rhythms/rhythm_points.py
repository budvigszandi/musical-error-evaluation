from enum import IntEnum

class RhythmPoints(IntEnum):
  CORRECT_RHYTHM_POINT = 10
  DELETED_RHYTHM_POINT = - CORRECT_RHYTHM_POINT
  INSERTED_RHYTHM_POINT = - CORRECT_RHYTHM_POINT
  SUBSTITUTED_RHYTHM_POINT = - CORRECT_RHYTHM_POINT / 2

  DIFFERENT_TYPE_POINT = SUBSTITUTED_RHYTHM_POINT
  LENGTH_DIFFERENCE_WEIGHT = 1